public class MainClass01 {
	// 프로그램 시작 메인메소드
	public static void main(String[] args) {
		// 정수형 변수 선언하고 값 대입하기.
		int numInt = 10;
		
		// 실수형 변수 선언하고 값 대입하기
		double numDouble = 10.1;
		
		System.out.println(numInt);
		System.out.println(numDouble);
	}
}
